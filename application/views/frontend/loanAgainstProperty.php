															<p>However smart investor and saver you are, there can be times when your cash flows are disturbed and financial crunch becomes a reality. Though, taking help of friends and family is an easy way but problems come when the requirement is high. In such case, it is a better option to leverage on your idle lying asset like a residential flat, any shop, an office or even a piece of land by offering it as collateral to the bank to avail a loan called LAP.</p>
													<p><strong>Benefits:</strong></p>
																		<p>LAP is generally compared with a Personal Loan because end usage in both are not tracked but due to following advantages, a LAP loan scores over a PL.</p>
																		<p><ul style="padding-left:15px;">
																		<li>Interest rate for a LAP ranges between 12- 16% which is fairly lower than PL interest rates which varies in 14-22% range.</li>
																		<li>LAP tenure can go upto 15 years whereas a PL can be extended till 5 years only.</li>
																		<li>Loan eligibility is largely driven by property valuation whereas in PL, amount of loan get restricted by the individual's income.</li>
																		</ul>
																		</p>																		<p><strong>Documents:</strong></p>

																		<p>Majority of the documents required are very similar to any other loan like:</p>
																		<p><ul style="padding-left:15px;">
																		<li>ID Proof Copy</li>
																		<li>Address Proof Copy</li>
																		<li>3-4 months salary slips along with latest 1-2 years Form 16 copy for Salaried Applicant OR latest 2 years certified financial statement for Self Employed applicants</li>
																		</ul>
																		</p>
																		<p>Now, along with above basic documents, more important are property documents. Property offered for collateral should be clear freehold property with no encumbrances </p>
																		<p><strong>Eligibility</p></strong>

																		<p>Eligibility for the LAP is determined by aggregation of many parameters like:</p>
																		<p><ul style="padding-left:15px;">

																		<li>Income of applicant which determined his/her repayment power</li>
																		<li>Existing fixed obligations like running EMIs which further reduces his/her repayment capability</li>
																		<li>Cost/Market Value of the property given for collateral</li>
																		<li>Credit repayment track record of the individual, popularly known as CIBIL score.</li>
																		</ul>
																		</p>
