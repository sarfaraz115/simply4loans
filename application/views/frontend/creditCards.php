											<p><strong>Why Credit Card?</strong></p>
												<p>

												Eligibility criteria vary very strongly for the different cards. Based on Credit Limit offer  and other various benefits, the eligibility criteria is differently set by the issuer. For example, for a basic card in any bank, minimum take home salary requirement will be a 20000 per month, age of 21 years and a healthy credit score. Being an unsecured type of short term loan, issuer go very safely while granting approval to credit card applications.

												</p>
										  <p><strong>Advice</strong><br>

													<p><strong style="font-size:13px;">Don't Overuse:</strong> Since the credit is revolving in nature and very handy, we easily fall in the trap of spending more than required or spending more than we can pay back. Ultimately, credit card outstanding is a liability & nonpayment hits the credit score badly.  </p>

													<p><strong style="font-size:13px;">Always Pay On Time:</strong> If for any reason you miss the payment, the penalty is as high as 36-40% per annum interest charges on outstanding. One big payment miss can wipe out all the benefits accrued & enjoyed in complete year sometime so be vigilant on timely payments.</p>

													<p><strong style="font-size:13px;">Choose The Card:</strong> One shouldn't go for any card just because it says free. Select the card which suits your requirement and give benefits based on your spending style. There are specialized cards for request flyers, doctors, fuel consumption, shopping etc.</p>

													<p><strong style="font-size:13px;">Read Benefits & Document Carefully:</strong> Read properly about the benefits for which you are taking that card, there may be conditions, upper limits etc. You should read carefully all documents for charges and fees. Whether it can be used for International Transactions, what is the cash limit etc.</p>
											<p><strong>Documents Required</strong><br>

													Documents required are normal one used for any banking purpose:<br />

												<ul style="padding-left:20px;">
													<li>Identity Proof</li>
													<li>Address Proof</li>
													<li>Income Proof (Latest Salary Slips for Salaried Person, Latest ITR & Business Continuity  Proof in case of Self Employed)</li>
													</ul>
