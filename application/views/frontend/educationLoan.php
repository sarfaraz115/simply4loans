                                                   <li>There is provision of repayment relaxation of 6 months to 1 year after completion of course. This given student time to find a job and settle financially before start paying back.</li>
                                                   <li>Interest repaid by the student is completely exempted from taxation under Section 80-E hence; the effective rate borrowing reduces considerably making it further attractive.</li>
                                                   <li>These loans cover fees for tuition, examination, library, laboratory and hostel; money for purchasing books, equipment, instruments and uniform; travel expenses for studies abroad; caution deposit or refundable deposit, etc.</li>
