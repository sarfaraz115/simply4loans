                                                   With rising education costs, it becomes difficult for any parent to entirely fund the expensive education program of the child. For example, a 4 year B.Tech programme in India can range between 5-10 Lacs while a two years MBA from premier institute like IIM can dent upto 12 to 20 Lacs in parents pocket. Studying abroad is anyways known to be expensive and can cost upto 50-60 Lacs.
