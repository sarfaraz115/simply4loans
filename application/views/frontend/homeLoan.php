																			<li>b. Latest 1-2 previous year form 16/ITR AND </li>
																			<li>c. Latest 6 months salary account statement</li>
																		</ul>

																		<p><strong>Self Employed Applicant-</strong></p>

																		<p>All documents remain same as that for salaried applicant except for proofs of income which are the following:</p>

