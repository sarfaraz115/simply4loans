																		<li>Income tax rebate on repayment of the interest is available under Section 24 for the self occupied property (max 1.5 lacs)</li>
																		<li>Additional tax exemption of Rs 1 Lac is given on interest repayment on any other property other than self-occupied property</li>
																			 </ul>
																	   <b>Eligibility</b>
																		Income: This is the most important parameter to judge the quantum of loan. The more the income, the more confidence bank has on your repayment.
																		Other Loan Holding: Eligibility for home loan decreases if there are existing loans as you need to pay those EMIs as well and thus; your effective income level goes down.
																		Credit History: Famously also known as CIBIL score, it help the bank in determining your track record for any other past loans.
																		Property Authenticity: Property for which the loan is taken should be legally and technically cleared by the local bodies and lawyers. It shouldn’t have any claims as it will be accepted as collateral for the home loan.

																		</p>
